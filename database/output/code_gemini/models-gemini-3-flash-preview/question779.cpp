#include <iostream>
#include <vector>

using namespace std;

int main() {
    // Standard competitive programming I/O optimization
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n;
    // Reading the number of locations (N)
    if (!(cin >> n)) return 0;

    // Reading the travel times for each segment
    vector<long long> t(n);
    for (int i = 0; i < n; ++i) {
        cin >> t[i];
    }

    // Reading the bus intervals for each stop
    vector<long long> interval(n);
    for (int i = 0; i < n; ++i) {
        cin >> interval[i];
    }

    long long current_time = 0;

    for (int i = 0; i < n; ++i) {
        // Ambrósio starts at the first location at time 0.
        // He doesn't need to wait for the first bus.
        // For subsequent locations (i > 0), he must wait for the next bus departure.
        if (i > 0) {
            long long rem = current_time % interval[i];
            if (rem != 0) {
                // If current_time is not a multiple of the bus interval,
                // wait until the next multiple of interval[i].
                current_time += (interval[i] - rem);
            }
        }
        
        // Add the travel time to the next destination
        current_time += t[i];
    }

    // Final result is the total time spent
    cout << current_time << endl;

    return 0;
}

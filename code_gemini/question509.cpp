int main() {
    int x, y;
    std::cin >> x >> y;
    std::cout << (x > y) << std::endl;
    std::cout << (x == y) << std::endl;
    std::cout << (x < y) << std::endl;
    std::cout << (x != y) << std::endl;
    std::cout << (x >= y) << std::endl;
    std::cout << (x <= y) << std::endl;
    return 0;
}

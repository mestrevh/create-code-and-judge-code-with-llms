#include <iostream>
#include <vector>

int main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(NULL);

    int n;
    std::cin >> n;

    std::vector<std::vector<int>> matrix(n, std::vector<int>(n));
    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < n; ++j) {
            std::cin >> matrix[i][j];
        }
    }

    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < n; ++j) {
            int value = matrix[j][i];
            if (value < 0) {
                value *= 2;
            }
            std::cout << value;
            if (j < n - 1) {
                std::cout << " ";
            }
        }
        std::cout << "\n";
    }

    return 0;
}

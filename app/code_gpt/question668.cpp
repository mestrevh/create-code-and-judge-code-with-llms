#include <iostream>
#include <vector>

using namespace std;

vector<int> sieve(int n) {
    vector<bool> is_prime(n + 1, true);
    is_prime[0] = is_prime[1] = false;
    for (int i = 2; i * i <= n; i++) {
        if (is_prime[i]) {
            for (int j = i * i; j <= n; j += i) {
                is_prime[j] = false;
            }
        }
    }
    vector<int> primes;
    for (int i = 2; i <= n; i++) {
        if (is_prime[i]) primes.push_back(i);
    }
    return primes;
}

int main() {
    int N;
    cin >> N;

    int limit = 1.2 * N * log(N);  // Upper bound approximation
    vector<int> primes = sieve(limit);

    for (int i = 0; i < N; i++) {
        cout << primes[i] << endl;
    }

    return 0;
}
